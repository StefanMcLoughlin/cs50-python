# Frage nach dem Namen
name = input("Wie heisst Du? ").strip().title()
print(f"Willkommen, {name}")

# Frage nach dem Wohnort
city = input("Wo wohnst Du? ").strip().capitalize()
print(f"Du wohnst in {city}")

# Frage nach dem Lieblingsessen
food = input("Was ist Dein Lieblingsessen? ").strip().capitalize()
print(f"Dein Lieblingsessen ist {food}")